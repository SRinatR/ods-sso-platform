export function transliterateCyrillicName(value: string) {
  return value
    .split("")
    .map((char) => transliteration[char] ?? char)
    .join("")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase()
    .replace(/(^|[\s'’-])([a-z])/g, (_match, prefix: string, letter: string) => {
      return `${prefix}${letter.toUpperCase()}`;
    });
}
